n = int(input("Введіть, будь яке число!"))
if n>0:
    print("Це число є натуральним!")
    if (n % 2) == 0:
        print("Число парне!")
        if (n % 4) == 0:
            print("Кратне 4!")
    else:
        print("Не парне, а відповідно не може бути кратне 4")
else:
    print("Це число не є натуральним!")